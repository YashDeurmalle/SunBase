package com.sunbase.CustomerApp.util;
/**
 * Interface defining constants used across the CustomerApp application.
 * It includes success and failure codes, success and failure status messages,
 * and a constant for technical error messages.
 */
public interface Constants {
    /** Constant representing a success code. */
    public static final String successCode = "0";

    /** Constant representing a failure code. */
    public static final String failureCode = "1";

    /** Constant representing a success status message. */
    public static final String successStatus = "success";

    /** Constant representing a failure status message. */
    public static final String failureStatus = "failure";

    /** Constant representing a technical error message. */
    public static final String TECHNICAL_ERROR = "Technical Error Occurred!";
}
